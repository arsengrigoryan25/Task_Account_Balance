package com.example.DTO;

public class Balance {
    private String name;
    private double value;

    public Balance() {}
    public Balance(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}
